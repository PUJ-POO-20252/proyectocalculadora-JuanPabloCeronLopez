// --- Definiciones (El "como") ---
int sumar (int a, int b) {
    return a + b;
}

int restar (int a, int b) {
    return a - b;
}

int division (int a, int b) {
    return a / b;
}

int multiplicar (int a, int b) {
    return a * b;
}